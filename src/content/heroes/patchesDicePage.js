function createDieSection(entry) {
  return {
    id: entry.id,
    title: entry.title,
    description: `Ветка: ${entry.branch}`,
    blocks: [
      {
        id: `${entry.id}-usage`,
        type: 'text',
        title: 'Как читать эту кость',
        paragraphs: [
          ...(entry.placement ? [`Куда кладётся: ${entry.placement}`] : []),
          `Что это за кость: ${entry.what}`,
          `Практика: ${entry.practice}`,
        ],
      },
      {
        id: `${entry.id}-die`,
        type: 'die',
        heroDieIndex: entry.dieIndex,
        featuredFaceIndex: entry.featuredFaceIndex ?? 0,
        faceDetails: entry.faceDetails,
        eyebrow: 'Кубик и грани',
        title: 'Главная грань и все 6 граней',
        text: 'Сначала показана одна главная грань крупно, ниже — все грани этой кости с подписями и пояснениями.',
        caption: 'Главная грань — быстрый якорь по кости. Ниже — полный набор граней для чтения по ходу партии.',
      },
      ...(entry.errors?.length
        ? [
            {
              id: `${entry.id}-errors`,
              type: 'checklist',
              title: 'Частые ошибки',
              items: entry.errors,
            },
          ]
        : []),
    ],
  }
}

