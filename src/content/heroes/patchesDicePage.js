function createDiceSection(entry) {
  const blocks = [
    {
      id: `${entry.id}-die`,
      type: 'die',
      heroDieIndex: entry.dieIndex,
      eyebrow: entry.branch,
      title: entry.title,
      text: `${entry.short} ${entry.what}`.trim(),
      caption: 'Выше — реальная картинка кубика и его 6 граней из src/assets/dices/patches/.',
    },
    {
      id: `${entry.id}-usage`,
      type: 'text',
      title: 'Как читать эту кость',
      paragraphs: [
        ...(entry.placement ? [`Куда кладётся: ${entry.placement}`] : []),
        `Практика: ${entry.practice}`,
      ],
    },
    {
      id: `${entry.id}-faces`,
      type: 'list',
      title: 'Расшифровка граней',
      items: entry.faces,
    },
  ]

  if (entry.errors?.length) {
    blocks.push({
      id: `${entry.id}-errors`,
      type: 'checklist',
      title: 'Частые ошибки',
      items: entry.errors,
    })
  }

  return {
    id: entry.id,
    title: entry.title,
    description: `Ветка: ${entry.branch}`,
    blocks,
  }
}

