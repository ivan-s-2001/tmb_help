function createDiceSection(entry) {
  const blocks = [
    {
      id: `${entry.id}-die`,
      type: 'die',
      heroDieIndex: entry.dieIndex,
      eyebrow: entry.branch,
      title: entry.title,
      text: entry.short,
      caption: entry.what,
      featuredFaceIndex: entry.featuredFaceIndex ?? 0,
      featuredFaceLabel: 'Главная грань',
      showFeaturedFace: true,
      showAllFaces: true,
      faceDetails: entry.faceDetails,
    },
    {
      id: `${entry.id}-usage`,
      type: 'text',
      title: 'Как читать эту кость',
      paragraphs: [
        ...(entry.placement ? [`Куда кладётся: ${entry.placement}`] : []),
        `Практика: ${entry.practice}`,
      ],
    },
  ]

  if (entry.errors?.length) {
    blocks.push({
      id: `${entry.id}-errors`,
      type: 'checklist',
      title: 'Частые ошибки',
      items: entry.errors,
    })
  }

  return {
    id: entry.id,
    title: entry.title,
    description: `Ветка: ${entry.branch}`,
    blocks,
  }
}

